Key? getDashboardIconKey(int index) {
  if (index == 0) return homeIconKey;
  if (index == 1) return productIconKey;
  if (index == 2) return celebrityIconKey;
  if (index == 3) {
    return moreIconKey;
  } else {
    return null;
  }
}

Key? getDataSheetKey(String name) {
  if (name == 'transfer') return transferKey;
  if (name == 'deposit') return depositKey;
  if (name == 'withdraw') return withdrawKey;
  if (name == 'qr pay') return qrCoderKey;
  if (name == 'history') return historyKey;
  if (name == 'salary') return salaryKey;
  if (name == 'request') return requestKey;
  if (name == 'bill') return billKey;
  if (name == 'gift') return giftKey;
  if (name == 'saving') return savingKey;
  if (name == 'budget') {
    return budgetKey;
  } else {
    return UniqueKey();
  }
}

